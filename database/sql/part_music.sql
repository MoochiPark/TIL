CREATE TABLE PART_MUSIC (
  PM_MSC_CODE VARCHAR(20),
  PM_EMP_CODE VARCHAR(20),
  PM_EMP_ROLE VARCHAR(20) NOT NULL,
  PM_EMP_FEE INT,
  CONSTRAINT pm_msc_code_fk FOREIGN KEY(PM_MSC_CODE) 
  REFERENCES MUSIC(MSC_CODE),
  CONSTRAINT pm_emp_code_fk FOREIGN KEY(PM_EMP_CODE) 
  REFERENCES EMPLOYEE(EMP_CODE)
);


